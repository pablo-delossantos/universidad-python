# Variables en Python

# Declaración e inicialización de variables

edad = 28
altura = 1.65
pais = "México"

# Acceder a las variables
print("Valores iniciales")
print("Edad:", edad)
print("Altura:", altura)
print("País:", pais)

# Modificar el valor de una variable
edad = 30
altura = 1.68

print("Valores modificados")
print("Edad:", edad)
print("Altura:", altura)
print("País:", pais)

# En python el tipo es dinámico
edad = "treinta"
print("Edad:", edad)

# Si queresmo acceder a una variable no declarada manda error
telefono = "55443322"
print("Telefono:", telefono)